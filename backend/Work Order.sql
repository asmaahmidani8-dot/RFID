    AND NVL(wop.QUANTITY_COMPLETED, 0) < wdj.START_QUANTITY

ORDER BY 
    wdj.SCHEDULED_COMPLETION_DATE ASC,
    wen.WIP_ENTITY_NAME ASC,
    wop.OPERATION_SEQ_NUM ASC
;


